package com.shivaa.basics;

public class Operators {
    public static void main(String[] args) {
        // comparison operator -> (<,>,==,<=,>=)
        int a = 7;
        int b = 6;

        if (a > b) {
            System.out.println(a +" is greator than "+b);
        } else if (a < b) {
            System.out.println(b +" is greator than "+a);
        } if (a == b) {
            System.out.println(a +" is equal to "+b);
        }


    }
}

